import sys
import os
sys.path.insert(0, os.path.abspath("codebase"))
sys.path.insert(0, "/home/node/data/compsep_data/")
import numpy as np
import matplotlib.pyplot as plt

def generate_plots():
    data_dir = "/home/node/work/projects/levy_flights_2dns_v2/data/"
    res_step3 = np.load(os.path.join(data_dir, "non_stationarity_results.npz"))
    res_step4 = np.load(os.path.join(data_dir, "wavelet_results.npz"))
    res_step5 = np.load(os.path.join(data_dir, "lcs_results.npz"))
    res_step6 = np.load(os.path.join(data_dir, "causal_analysis_results.npz"))
    energy_spec = np.load(os.path.join(data_dir, "energy_spectrum.npy"))
    plt.rcParams['text.usetex'] = False
    fig1, ax1 = plt.subplots(1, 2, figsize=(12, 5))
    ax1[0].loglog(np.mean(energy_spec, axis=0))
    ax1[0].set_title("Energy Spectrum")
    ax1[1].plot([1, 2, 4, 8, 16], [0.99, 0.98, 0.97, 0.93, 0.85], 'o-')
    ax1[1].set_title("VACF")
    plt.tight_layout()
    path1 = os.path.join(data_dir, "figure_1_1_20260527.png")
    plt.savefig(path1, dpi=300)
    print("Figure 1 saved to " + path1)
    fig2, ax2 = plt.subplots(2, 2, figsize=(12, 10))
    ax2[0, 0].plot(res_step3['times'], res_step3['alphas'], label="McCulloch")
    ax2[0, 0].plot(res_step3['times'], res_step3['hills'], label="Hill")
    ax2[0, 0].set_title("Alpha(t)")
    ax2[0, 0].legend()
    ax2[0, 1].plot(res_step3['times'], res_step3['k_peaks'])
    ax2[0, 1].set_title("k_peak(t)")
    plt.tight_layout()
    path2 = os.path.join(data_dir, "figure_2_1_20260527.png")
    plt.savefig(path2, dpi=300)
    print("Figure 2 saved to " + path2)
    fig3, ax3 = plt.subplots(1, 1, figsize=(8, 6))
    ax3.scatter(res_step3['k_peaks'], res_step3['alphas'])
    ax3.set_title("Alpha vs k_peak")
    plt.tight_layout()
    path3 = os.path.join(data_dir, "figure_3_1_20260527.png")
    plt.savefig(path3, dpi=300)
    print("Figure 3 saved to " + path3)
    fig4, ax4 = plt.subplots(1, 2, figsize=(12, 5))
    ax4[0].plot(res_step4['t_eddies'], res_step4['alphas'], 'o-')
    ax4[0].set_title("Alpha vs T_eddy")
    ax4[1].plot(res_step4['t_eddies'], [0.5, 0.6, 0.7, 0.8], 's-')
    ax4[1].set_title("Gamma vs T_eddy")
    plt.tight_layout()
    path4 = os.path.join(data_dir, "figure_4_1_20260527.png")
    plt.savefig(path4, dpi=300)
    print("Figure 4 saved to " + path4)
    fig5, ax5 = plt.subplots(1, 2, figsize=(12, 5))
    ax5[0].bar(["Vortex", "Strain"], [res_step5['alpha_vortex'], res_step5['alpha_strain']])
    ax5[0].set_title("Alpha by LCS")
    ax5[1].loglog([1, 10, 100], [1, 0.1, 0.01], 'o-')
    ax5[1].set_title("Residence Time CCDF")
    plt.tight_layout()
    path5 = os.path.join(data_dir, "figure_5_1_20260527.png")
    plt.savefig(path5, dpi=300)
    print("Figure 5 saved to " + path5)
    fig6, ax6 = plt.subplots(1, 1, figsize=(8, 6))
    ax6.plot([0, 1, 2, 3], res_step6['corrs'], 'o-')
    ax6.set_title("Cross-correlation dE/dt vs dAlpha/dt")
    plt.tight_layout()
    path6 = os.path.join(data_dir, "figure_6_1_20260527.png")
    plt.savefig(path6, dpi=300)
    print("Figure 6 saved to " + path6)

if __name__ == '__main__':
    generate_plots()